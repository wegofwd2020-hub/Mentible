#!/usr/bin/env node
/* global __dirname */
// Regenerates src/components/dompurifySource.ts from the INSTALLED dompurify
// package, so the WebView can inline the library (spec D-I6 / #325 — a reader
// that fetches its sanitizer from a CDN shows nothing offline).
//
// Why a generated .ts file and not a raw asset require(): Metro's asset
// pipeline (`assetExts`) hands back a resolved module/URI for the bundler and
// for native, not the file's raw text as a JS string, and there is no
// equivalent transform wired into this project's Jest config either — so
// `require(".../purify.min.js.txt")` would not yield a usable string in
// either runtime. A plain exported string constant needs no special loader in
// Metro OR Jest: it is just JS, understood identically everywhere. The
// counterpart test (`__tests__/components/dompurifySource.test.ts`) asserts
// this generated constant stays byte-identical to the installed package, so a
// `dompurify` bump can't silently drift from what ships in the WebView.
//
// Run after bumping the `dompurify` dependency:
//   node scripts/gen-dompurify-source.js

const fs = require("fs");
const path = require("path");

const SRC = path.join(__dirname, "..", "node_modules", "dompurify", "dist", "purify.min.js");
const OUT = path.join(__dirname, "..", "src", "components", "dompurifySource.ts");
const { version } = require("../node_modules/dompurify/package.json");

const source = fs.readFileSync(SRC, "utf8");

if (source.includes("</script")) {
  // Would break out of the <script> tag it gets embedded in verbatim
  // (contentHtml.ts does not JSON-escape this one — it is OUR trusted build
  // output, not chapter content). Fail loudly rather than ship a broken doc.
  throw new Error(
    "purify.min.js contains a literal '</script' — cannot inline verbatim; " +
      "switch dompurifySource.ts to embed it JSON-escaped instead.",
  );
}

const out = `// AUTO-GENERATED by scripts/gen-dompurify-source.js — do not hand-edit.
// Source: node_modules/dompurify/dist/purify.min.js (dompurify@${version}).
// Regenerate after bumping the `+"`dompurify`"+` dependency:
//   node scripts/gen-dompurify-source.js
// __tests__/components/dompurifySource.test.ts asserts this string stays
// byte-identical to the installed copy, so a version bump can't silently
// drift from what actually ships in the WebView (Task 6 / spec D-I6).

export const DOMPURIFY_SRC: string = ${JSON.stringify(source)};

export const DOMPURIFY_VERSION = ${JSON.stringify(version)};
`;

fs.writeFileSync(OUT, out);
console.log(`Wrote ${OUT} (${source.length} bytes of dompurify@${version} inlined)`);
